# Вычисление суммы на счету в конце срока вклада
from decimal import Decimal as D

# ввод первоначальной суммы
while True:
    P = input("\nВведите сумму первоначальных инвестиций (положительное целое число, например, 20000):")
    if P.isdigit():
        P = D(P)
        break
# print(P)

