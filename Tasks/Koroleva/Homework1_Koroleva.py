while True:
    n = input("\nВведите количество периодов начисления ставки в год (от 1 до 12, для ежемесячной капитализации - 12):")
    if n.isdigit() and int(n) <= 12 and int(n) != 0 :
        n = int(n)
        break
# print(n)

